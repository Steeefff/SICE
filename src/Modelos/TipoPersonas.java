public class TipoPersonas {

    private String rol;
    private int idPermiso, id;

    public TipoPersonas(){}
    
    public TipoPersonas(int id, String rol, int idPermiso) {
        this.rol = rol;
        this.idPermiso = idPermiso;
        this.id=id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public int getIdPermiso() {
        return idPermiso;
    }

    public void setIdPermiso(int idPermiso) {
        this.idPermiso = idPermiso;
    }
    
    
}
